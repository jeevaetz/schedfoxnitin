    /** Creates new form EmployeeTestPanel */
    public EmployeeTestPanel(EmployeeTestScore score, Schedule_View_Panel svp) {
        initComponents();

        testNameLabel.setText(score.getEmployeeTest(svp.getCompany()).getEmployeeTestDisplay());
        try {
            testDateLabel.setText(format.format(score.getDateTaken()));
            testScoreLabel.setText(score.getScore() + "");
        } catch (Exception e) {
            
            EmployeeTest test = score.getEmployeeTest(svp.getCompany());
            if (test.isDefault_to_na()) {
                testScoreLabel.setText("Passed");
                testDateLabel.setText("Completed");
            } else {
                testScoreLabel.setText("");
                testDateLabel.setText("Never Taken");
            }
        }
    }

