    public void setAlertData(HashMap<Integer, LateCheckinInfo> lateCheckinInfo) {
        Iterator<Integer> keys = lateCheckinInfo.keySet().iterator();
        while (keys.hasNext()) {
            Integer key = keys.next();
            LateCheckinInfo lateCheckin = lateCheckinInfo.get(key);
            LateCheckinCompanyPanel panel = new LateCheckinCompanyPanel();
            panel.updateCompanyInfo(lateCheckin);
            lateCheckinPanel.add(panel);
        }
    }
    
