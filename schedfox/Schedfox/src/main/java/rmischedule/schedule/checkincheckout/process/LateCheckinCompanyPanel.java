    public void updateCompanyInfo(LateCheckinInfo lateCheckinInfo) {
        clientNameLabel.setText(lateCheckinInfo.getCompany().getCompName());
        ClientRowPanel headerRow = new ClientRowPanel();
        companyPanel.add(headerRow);
        for (int l = 0; l < lateCheckinInfo.getSchedules().size(); l++) {
            ScheduleData data = lateCheckinInfo.getSchedules().get(l);
            ClientRowPanel clientRow = new ClientRowPanel();
            companyPanel.add(clientRow);
            clientRow.updateClientInfo(data, lateCheckinInfo.getCompany());
        }
    }

