    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            if (option == null) {
                option = new HealthCareOption();
            }
            option.setActive(true);
            option.setName(healthCareTxt.getText());
            HealthCareController.getInstance(companyId).saveHealthCareOption(option);
            option = null;
            reloadData();
        } catch (Exception exe) {
            exe.printStackTrace();
        }
    }//GEN-LAST:event_saveBtnActionPerformed

