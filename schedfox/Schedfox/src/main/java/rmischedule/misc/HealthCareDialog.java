    private class HealthCareModel extends AbstractTableModel {

        private ArrayList<HealthCareOption> healthOptions;
        
        public HealthCareModel() {
            healthOptions = new ArrayList<HealthCareOption>();
        }
        
        public int getRowCount() {
            return healthOptions.size();
        }

        public int getColumnCount() {
            return 1;
        }
        
        public HealthCareOption get(int i) {
            return this.healthOptions.get(i);
        }

        public void add(HealthCareOption option) {
            this.healthOptions.add(option);
            this.fireTableDataChanged();
        }
        
        public void clear() {
            this.healthOptions.clear();
            this.fireTableDataChanged();
        }
        
        public Object getValueAt(int rowIndex, int columnIndex) {
            return healthOptions.get(rowIndex).getName();
        }
        
        @Override
        public String getColumnName(int columnIndex) {
            return "Health Care Name";
        }
    }
    
