    private void reloadData() {
        try {
            healthModel.clear();
            ArrayList<HealthCareOption> options = HealthCareController.getInstance(companyId).getHealthCareOptions();
            for (int o = 0; o < options.size(); o++) {
                healthModel.add(options.get(o));
            }
            showInformation();
        } catch (Exception exe) {
            exe.printStackTrace();
        }
    }
    
    private void showInformation() {
        try {
            healthCareTxt.setText(option.getName());
        } catch (Exception exe) {
            healthCareTxt.setText("");
        }
    }
    
