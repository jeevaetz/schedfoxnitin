    private void clearData() {
        this.employeeWageType = new EmployeeWageTypes();
        this.displayData();
    }

    private void displayData() {
        this.descriptionTxt.setText(employeeWageType.getDescription());
        this.oneTimeChk.setSelected(employeeWageType.getOneTime());

        BillingController billingController = BillingController.getInstance(this.companyId);
        try {
            wageModel.clearData();
            ArrayList<EmployeeWageTypes> types = billingController.getEmployeeWageTypes();
            for (int t = 0; t < types.size(); t++) {
                wageModel.addWageType(types.get(t));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(Main_Window.parentOfApplication, "Error gettting wage types");
        }
    }

    private EmployeeWageTypeModel getTableModel() {
        return this.wageModel;
    }

