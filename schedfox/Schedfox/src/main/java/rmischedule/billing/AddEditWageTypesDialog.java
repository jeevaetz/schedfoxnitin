    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        employeeWageType.setDescription(this.descriptionTxt.getText());
        employeeWageType.setOneTime(this.oneTimeChk.isSelected());
        BillingController billingController = BillingController.getInstance(this.companyId);
        try {
            billingController.saveEmployeeWageType(employeeWageType);
            this.clearData();
            JOptionPane.showMessageDialog(Main_Window.parentOfApplication, "Data Saved!",
                    "Saved", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(Main_Window.parentOfApplication, "Could not save data!",
                    "Error!", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_saveBtnActionPerformed

