    public ClientTerminationConfirmation(java.awt.Frame parent, boolean modal, String companyId, Integer clientId) {
        super(parent, modal);
        initComponents();
        this.companyId = companyId;
        this.clientId = clientId;
        ClientController clientController = ClientController.getInstance(companyId);
        try {
            ArrayList<TerminationReason> reasons = clientController.getTerminationReasons();
            for (int r = 0; r < reasons.size(); r++) {
                terminationReasonCmbo.addItem(reasons.get(r));
            }
        } catch (Exception exe) {}
        calTermDate = new rmischedule.components.jcalendar.JCalendarPanel();
        pnCal.add(calTermDate, BorderLayout.CENTER);
        calTermDate.setEnabled(true);
        setLocation((parent.getWidth()/2) - 100, (parent.getHeight()/2) - 100);
    }
    
