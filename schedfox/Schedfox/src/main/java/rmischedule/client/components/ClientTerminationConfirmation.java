    private void pbOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pbOkActionPerformed
        terminationDate = calTermDate.getCalendar();
        
        try {
            ClientController clientController = ClientController.getInstance(companyId);
            
            ClientTerminationReason clientTermReason = new ClientTerminationReason();
            clientTermReason.setClientId(this.clientId);
            clientTermReason.setNotes(termNotesTxt.getText().trim());
            clientTermReason.setTerminationReasonId(((TerminationReason)terminationReasonCmbo.getSelectedItem()).getTerminationReason());
            
            clientController.saveClientTerminationReason(clientTermReason);
        } catch (Exception exe) {}
        
        dispose();
    }//GEN-LAST:event_pbOkActionPerformed
    
