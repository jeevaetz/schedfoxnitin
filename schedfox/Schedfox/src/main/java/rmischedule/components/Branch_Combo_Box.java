    /** Creates new form Branch_Combo_Box */
    public Branch_Combo_Box(Company_Combo_Box myComp) {
        initComponents();
        myCompanyBox = myComp;
        myCompanyBox.setBranchBoxCompanyBox(this);
        populateListForSelectedCompany();
    }
    
    public void populateListForSelectedCompany() {
        Company myComp = myCompanyBox.getSelectedCompany();
        myBranches = myComp.getBranches();
        myComboBox.removeAllItems();
        for (int i = 0; i < myBranches.size(); i++) {
            myComboBox.addItem(myBranches.get(i).getBranchName());
        }
    }
    
    public Branch getSelectedBranch() {
        return myBranches.get(myComboBox.getSelectedIndex());
    }
    
