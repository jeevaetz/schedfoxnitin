    public String getValue() {
        if (myCal instanceof JCalendarComboBox) {
            return myFormat.format(((JCalendarComboBox)myCal).getCalendar().getTime());
        } else {
            return ((JLabel)myCal).getText();
        }
    }

    public void setValue(DynamicFieldValue value) {
        Date dateToSet = new Date();
        Calendar myCalendar = Calendar.getInstance();
        try {
            dateToSet = myFormat.parse(value.getDynamic_field_value());
        } catch (Exception e) {
        
        }
        myCalendar.setTime(dateToSet);
        try {
            if (myCal instanceof JCalendarComboBox) {
                ((JCalendarComboBox)myCal).setCalendar(myCalendar);
            } else {
                ((JLabel)myCal).setText(value.getDynamic_field_value());
            }
        } catch (Exception e) {}
    }

