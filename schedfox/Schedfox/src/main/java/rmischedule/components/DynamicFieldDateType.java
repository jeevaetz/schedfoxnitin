    private JComponent myCal;
    private SimpleDateFormat myFormat = new SimpleDateFormat("MM/dd/yyyy");

    /** Creates new form DynamicFieldDateType */
    public DynamicFieldDateType(boolean enabled) {
        initComponents();

        if (enabled) {
            myCal = new JCalendarComboBox();
        } else {
            myCal = new JLabel();
        }
        this.add(myCal);
    }

