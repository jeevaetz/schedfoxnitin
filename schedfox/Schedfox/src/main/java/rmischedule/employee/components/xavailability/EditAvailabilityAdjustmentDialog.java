    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            TimeOffAdjustment timeOffAdjustment = new TimeOffAdjustment();
            timeOffAdjustment.setAdjustedBy(Integer.parseInt(Main_Window.parentOfApplication.getUser().getUserId()));
            if (reasonTxt.getText().trim().length() > 0) {
                timeOffAdjustment.setAdjustmentNotes(reasonTxt.getText());
            } else {
                throw new Exception("Please enter a reason for adjustment!");
            }
            Date currentDate = new Date(new Connection().getServerTimeMillis());
            Employee emp = (Employee)this.employeeEdit.getSelectedObject();
            timeOffAdjustment.setDateAdded(currentDate);
            timeOffAdjustment.setEmployeeId(emp.getEmployeeId());
            timeOffAdjustment.setTimeOffTypeId(this.typeEmployee);
            int adjustment = (Integer)adjustmentSpinner.getValue();
            if (adjustment != 0) {
                timeOffAdjustment.setAdjustment(adjustment);
            } else {
                throw new Exception("Must choose a non zero adjustment!");
            }
            try {
                TimeOffController timeController =
                        TimeOffController.getInstance(this.employeeEdit.getConnection().myCompany);
                timeController.saveTimeOffAdjustment(timeOffAdjustment);

                JOptionPane.showMessageDialog(Main_Window.parentOfApplication,
                        "Adjustment Saved!", "Saved",
                        JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                this.tempAvail.loadData();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(Main_Window.parentOfApplication,
                        "Problem saving time off adjustment!", "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception exe) {
            JOptionPane.showMessageDialog(Main_Window.parentOfApplication,
                        "Error: " + exe.getMessage(), "Error",
                        JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveBtnActionPerformed

