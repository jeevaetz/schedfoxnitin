    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        AvailabilityNotes availNotes = new AvailabilityNotes();
        availNotes.setEnteredBy(Integer.parseInt(Main_Window.parentOfApplication.getUser().getUserId()));
        availNotes.setNote(noteText.getText());
        availNotes.setAvailabilityId(this.availId);

        AvailabilityController availController = AvailabilityController.getInstance(myParent.getConnection().myCompany);
        try {
            availController.saveAvailNote(availNotes);
            JOptionPane.showMessageDialog(Main_Window.parentOfApplication, "Note Saved!", "Note Saved!", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(Main_Window.parentOfApplication, "Could not save note!", "Could not save note!", JOptionPane.ERROR_MESSAGE);
        }

        Container myParent = this.getParent();
        myParent.remove(this);
        myParent.repaint();
    }//GEN-LAST:event_saveBtnActionPerformed

