    private void saveFollowupBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveFollowupBtnActionPerformed
        eventFollowup = new EventFollowup();
        eventFollowup.setEventId(this.eventId);
        eventFollowup.setFollowupNote(followupNoteTxt.getText());
        eventFollowup.setFollowupRequestedBy(Integer.parseInt(Main_Window.parentOfApplication.getUser().getUserId()));
        eventFollowup.setFollowupProcessedBy(Integer.parseInt(Main_Window.parentOfApplication.getUser().getUserId()));
        eventFollowup.setFollowupProcessedOn(new Date());
        try {
            eventFollowup.setFollowupType(((EventFollowType) followupCombo.getSelectedItem()).getEventFollowTypeId());
        } catch (Exception exe) {
        }
        this.dispose();
    }//GEN-LAST:event_saveFollowupBtnActionPerformed


